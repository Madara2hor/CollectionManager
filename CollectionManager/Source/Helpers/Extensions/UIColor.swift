import UIKit

extension UIColor {
    
    static var gradientDarkGrey: UIColor {
        return UIColor(
            red: 240 / 255,
            green: 240 / 255,
            blue: 240 / 255,
            alpha: 1
        )
    }
    
    static var gradientLightGrey: UIColor {
        return UIColor(
            red: 250 / 255,
            green: 250 / 255,
            blue: 250 / 255,
            alpha: 1
        )
    }
    
}
